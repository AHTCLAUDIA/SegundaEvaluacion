package poo.enumerados;

public class EjemploEnumerados {
    // declaro un enumerado dentro de una clase
    enum Meses {
        ENERO, FEBRERO, MARZO, ABRIL, MAYO, JUNIO
    }
}

// declaro un enumerado fuera de la clase pero en el mismo fichero
enum Dias {
    LUNES, MARTES, MIÉRCOLES, JUEVES, VIERNES, SÁBADO, DOMINGO
}
